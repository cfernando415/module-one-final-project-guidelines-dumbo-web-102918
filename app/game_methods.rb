def game(arg)
  prompt = TTY::Prompt.new
  animation(PERIODS)
  exclamation
  value = keypress
  if value == "try again"
    system("clear")
    game(arg)
  else
    fish = Pond.sample
    puts "yay! you caught a #{fish.species}"
    arg.fish << fish
  input = prompt.select("Would you like to keep playing, or view your basket?", %w(Keep_Playing View_Basket))
      if input == "Keep_Playing"
        game(arg)
      elsif input == "View_Basket"
        arg.view_basket(arg)
      end
  end
end

