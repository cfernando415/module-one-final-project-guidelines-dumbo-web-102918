def animation(array)
  prompt = TTY::Prompt.new
  i = rand(1..3)
  while i <  10
    print "\033[2J"
    array.each do |period|
      system("clear")
      puts period
      sleep(rand(0.3..0.75))
    end
    i += rand(1..2)
  end
end





